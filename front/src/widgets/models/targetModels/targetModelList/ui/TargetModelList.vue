<script setup lang="ts">
import {
  PToolboxTable,
  PHorizontalLayout,
  PButton,
  PButtonModal,
} from '@cloudforet-test/mirinae';
import { useTargetModelListModel } from '@/widgets/models/targetModels/targetModelList/model/targetModelListModel';
import { insertDynamicComponent, showErrorMessage, showSuccessMessage } from '@/shared/utils';
import DynamicTableIconButton from '@/shared/ui/Button/dynamicIconButton/DynamicTableIconButton.vue';
import { onBeforeMount, onMounted, reactive, watch } from 'vue';
import { useGetTargetModelList, useBulkDeleteTargetModel, useBulkDeleteTargetSoftwareModel, useBulkDeleteTargetOnPremModel } from '@/entities';

const { tableModel, initToolBoxTableModel, targetModelStore } =
  useTargetModelListModel();

interface IProps {
  trigger: boolean;
}

const props = defineProps<IProps>();

const emit = defineEmits(['select-row', 'update:trigger']);

const modals = reactive({
  alertModalState: { open: false },
  sourceModelAddModalState: { open: false },
});

const resGetTargetModelList = useGetTargetModelList();

onBeforeMount(() => {
  initToolBoxTableModel();
});

onMounted(function () {
  addDeleteIconAtTable.bind(this)();
  getTableList();
});

watch(
  () => props.trigger,
  () => {
    tableModel.tableState.selectIndex = [];
    getTableList();
    emit('update:trigger', false);
  },
);

function addDeleteIconAtTable() {
  const toolboxTable = this.$refs.toolboxTable.$el;
  const targetElement = toolboxTable.querySelector('.right-tool-group');
  const instance = insertDynamicComponent(
    DynamicTableIconButton,
    {
      name: 'ic_delete',
    },
    {
      click: () => {
        console.log('üîò [TargetModelList] ÏÇ≠Ï†ú Î≤ÑÌäº ÌÅ¥Î¶≠');
        console.log('üìä [TargetModelList] ÏÑ†ÌÉùÎêú Ìï≠Î™© Ïàò:', tableModel.tableState.selectIndex.length);
        console.log('üìã [TargetModelList] ÏÑ†ÌÉùÎêú Ïù∏Îç±Ïä§:', tableModel.tableState.selectIndex);
        
        if (tableModel.tableState.selectIndex.length > 0) {
          console.log('‚úÖ [TargetModelList] ÏÇ≠Ï†ú ÌôïÏù∏ Î™®Îã¨ Ïó¥Í∏∞');
          modals.alertModalState.open = true;
        } else {
          console.log('‚ö†Ô∏è [TargetModelList] ÏÑ†ÌÉùÎêú Ìï≠Î™©Ïù¥ ÏóÜÏñ¥ÏÑú Î™®Îã¨ÏùÑ Ïó¥ÏßÄ ÏïäÏùå');
        }
      },
    },
    targetElement,
    'prepend',
  );
  return instance;
}

function getTableList() {
  resGetTargetModelList.execute().then(res => {
    if (res.data.responseData) {
      targetModelStore.setTargetModel(res.data.responseData);
    }
  });
}

function handleSelectedIndex(selectedIndex: number) {
  const selectedData = tableModel.tableState.displayItems[selectedIndex];
  if (selectedData) {
    emit('select-row', { id: selectedData.id, name: selectedData.name });
  } else {
    emit('select-row', '');
  }
}

/**
 * Delete selected target models
 * ÏÑ†ÌÉùÎêú ÎåÄÏÉÅ Î™®Îç∏Îì§ÏùÑ ÏÇ≠Ï†úÌï©ÎãàÎã§.
 */
function multiDelete() {
  console.log('üóëÔ∏è [TargetModelList] multiDelete ÏãúÏûë');
  
  const selectedData = tableModel.tableState.selectIndex.map(index => {
    return tableModel.tableState.displayItems[index].id;
  });
  console.log('üìã [TargetModelList] ÏÑ†ÌÉùÎêú Îç∞Ïù¥ÌÑ∞ ID Î™©Î°ù:', selectedData);

  // Check if any selected models are software models
  const selectedModels = tableModel.tableState.selectIndex.map(index => {
    return tableModel.tableState.displayItems[index];
  });
  console.log('üîç [TargetModelList] ÏÑ†ÌÉùÎêú Î™®Îç∏ ÏÉÅÏÑ∏ Ï†ïÎ≥¥:', selectedModels);

  // Í∞Å Î™®Îç∏ÏùÑ ÌïòÎÇòÏùò Ïπ¥ÌÖåÍ≥†Î¶¨ÏóêÎßå Î∂ÑÎ•òÌïòÎèÑÎ°ù ÏàòÏ†ï
  const softwareModelIds: string[] = [];
  const cloudModelIds: string[] = [];
  const onPremModelIds: string[] = [];

  selectedModels.forEach(model => {
    const modelType = model?.modelType;
    
    // modelType Í∏∞Î∞òÏúºÎ°ú Î∂ÑÎ•ò
    if (modelType === 'SoftwareModel') {
      softwareModelIds.push(model.id);
      console.log(`üîß [TargetModelList] Î™®Îç∏ ${model?.id} (${model?.name}) - Software Î∂ÑÎ•ò (modelType)`);
    } else if (modelType === 'CloudModel') {
      cloudModelIds.push(model.id);
      console.log(`‚òÅÔ∏è [TargetModelList] Î™®Îç∏ ${model?.id} (${model?.name}) - Cloud Î∂ÑÎ•ò (modelType)`);
    } else if (modelType === 'OnPremiseModel') {
      onPremModelIds.push(model.id);
      console.log(`üè¢ [TargetModelList] Î™®Îç∏ ${model?.id} (${model?.name}) - OnPrem Î∂ÑÎ•ò (modelType)`);
    } else {
      // modelTypeÏù¥ ÏóÜÎäî Í≤ΩÏö∞ Í∏∞Ï°¥ ÏÜçÏÑ±ÏúºÎ°ú fallback
      const isSoftwareByTargetSoftwareModel = model?.targetSoftwareModel;
      const isSoftwareByMigrationType = model?.migrationType === 'software';
      const isSoftwareByName = model?.name?.toLowerCase().includes('sw') || false;
      
      const isSoftware = isSoftwareByTargetSoftwareModel || isSoftwareByMigrationType || isSoftwareByName;
      
      if (isSoftware) {
        softwareModelIds.push(model.id);
        console.log(`üîß [TargetModelList] Î™®Îç∏ ${model?.id} (${model?.name}) - Software Î∂ÑÎ•ò (fallback)`, {
          targetSoftwareModel: model?.targetSoftwareModel,
          migrationType: model?.migrationType,
          nameContainsSw: isSoftwareByName
        });
      } else {
        // SoftwareÍ∞Ä ÏïÑÎãå Í≤ΩÏö∞ CloudÎ°ú Î∂ÑÎ•ò (Í∏∞Î≥∏Í∞í)
        cloudModelIds.push(model.id);
        console.log(`‚òÅÔ∏è [TargetModelList] Î™®Îç∏ ${model?.id} (${model?.name}) - Cloud Î∂ÑÎ•ò (fallback)`, {
          targetSoftwareModel: model?.targetSoftwareModel,
          migrationType: model?.migrationType,
          nameContainsSw: isSoftwareByName
        });
      }
    }
  });

  console.log('üìä [TargetModelList] Î∂ÑÎ•ò Í≤∞Í≥º:', {
    softwareModelIds,
    cloudModelIds,
    onPremModelIds,
    softwareCount: softwareModelIds.length,
    cloudCount: cloudModelIds.length,
    onPremCount: onPremModelIds.length
  });

  const deletePromises = [];

  if (softwareModelIds.length > 0) {
    console.log('üöÄ [TargetModelList] Software Î™®Îç∏ ÏÇ≠Ï†ú API Ìò∏Ï∂ú:', softwareModelIds);
    const softwareDeletePromise = useBulkDeleteTargetSoftwareModel(softwareModelIds);
    console.log('üì° [TargetModelList] Software ÏÇ≠Ï†ú Promise Í∞ùÏ≤¥:', softwareDeletePromise);
    
    // Promise Ïã§Ìñâ Ï†ÑÏóê ÎÑ§Ìä∏ÏõåÌÅ¨ ÏöîÏ≤≠ Ï†ïÎ≥¥ Î°úÍπÖ
    softwareDeletePromise.then(responses => {
      console.log('üåê [TargetModelList] Software ÏÇ≠Ï†ú ÎÑ§Ìä∏ÏõåÌÅ¨ ÏùëÎãµ:', responses);
      responses.forEach((response, index) => {
        console.log(`üìä [TargetModelList] Software ÏÇ≠Ï†ú ÏùëÎãµ ${index + 1}:`, {
          status: response.status,
          statusText: response.statusText,
          method: response.config?.method?.toUpperCase(),
          url: response.config?.url,
          baseURL: response.config?.baseURL,
          fullURL: `${response.config?.baseURL}${response.config?.url}`,
          data: response.data
        });
      });
    }).catch(error => {
      console.error('‚ùå [TargetModelList] Software ÏÇ≠Ï†ú ÎÑ§Ìä∏ÏõåÌÅ¨ Ïò§Î•ò:', error);
      if (error.config) {
        console.log('üîç [TargetModelList] Software ÏÇ≠Ï†ú ÏöîÏ≤≠ Ï†ïÎ≥¥:', {
          method: error.config.method?.toUpperCase(),
          url: error.config.url,
          baseURL: error.config.baseURL,
          fullURL: `${error.config.baseURL}${error.config.url}`,
          data: error.config.data
        });
      }
    });
    
    deletePromises.push(softwareDeletePromise);
  }

  if (cloudModelIds.length > 0) {
    console.log('üöÄ [TargetModelList] Cloud Î™®Îç∏ ÏÇ≠Ï†ú API Ìò∏Ï∂ú (DeleteCloudModel):', cloudModelIds);
    const cloudDeletePromise = useBulkDeleteTargetModel(cloudModelIds);
    console.log('üì° [TargetModelList] Cloud ÏÇ≠Ï†ú Promise Í∞ùÏ≤¥:', cloudDeletePromise);
    
    // Promise Ïã§Ìñâ Ï†ÑÏóê ÎÑ§Ìä∏ÏõåÌÅ¨ ÏöîÏ≤≠ Ï†ïÎ≥¥ Î°úÍπÖ
    cloudDeletePromise.then(responses => {
      console.log('üåê [TargetModelList] Cloud ÏÇ≠Ï†ú ÎÑ§Ìä∏ÏõåÌÅ¨ ÏùëÎãµ:', responses);
      responses.forEach((response, index) => {
        console.log(`üìä [TargetModelList] Cloud ÏÇ≠Ï†ú ÏùëÎãµ ${index + 1}:`, {
          status: response.status,
          statusText: response.statusText,
          method: response.config?.method?.toUpperCase(),
          url: response.config?.url,
          baseURL: response.config?.baseURL,
          fullURL: `${response.config?.baseURL}${response.config?.url}`,
          data: response.data
        });
      });
    }).catch(error => {
      console.error('‚ùå [TargetModelList] Cloud ÏÇ≠Ï†ú ÎÑ§Ìä∏ÏõåÌÅ¨ Ïò§Î•ò:', error);
      if (error.config) {
        console.log('üîç [TargetModelList] Cloud ÏÇ≠Ï†ú ÏöîÏ≤≠ Ï†ïÎ≥¥:', {
          method: error.config.method?.toUpperCase(),
          url: error.config.url,
          baseURL: error.config.baseURL,
          fullURL: `${error.config.baseURL}${error.config.url}`,
          data: error.config.data
        });
      }
    });
    
    deletePromises.push(cloudDeletePromise);
  }

  if (onPremModelIds.length > 0) {
    console.log('üöÄ [TargetModelList] OnPrem Î™®Îç∏ ÏÇ≠Ï†ú API Ìò∏Ï∂ú (DeleteOnPremModel):', onPremModelIds);
    const onPremDeletePromise = useBulkDeleteTargetOnPremModel(onPremModelIds);
    console.log('üì° [TargetModelList] OnPrem ÏÇ≠Ï†ú Promise Í∞ùÏ≤¥:', onPremDeletePromise);
    
    // Promise Ïã§Ìñâ Ï†ÑÏóê ÎÑ§Ìä∏ÏõåÌÅ¨ ÏöîÏ≤≠ Ï†ïÎ≥¥ Î°úÍπÖ
    onPremDeletePromise.then(responses => {
      console.log('üåê [TargetModelList] OnPrem ÏÇ≠Ï†ú ÎÑ§Ìä∏ÏõåÌÅ¨ ÏùëÎãµ:', responses);
      responses.forEach((response, index) => {
        console.log(`üìä [TargetModelList] OnPrem ÏÇ≠Ï†ú ÏùëÎãµ ${index + 1}:`, {
          status: response.status,
          statusText: response.statusText,
          method: response.config?.method?.toUpperCase(),
          url: response.config?.url,
          baseURL: response.config?.baseURL,
          fullURL: `${response.config?.baseURL}${response.config?.url}`,
          data: response.data
        });
      });
    }).catch(error => {
      console.error('‚ùå [TargetModelList] OnPrem ÏÇ≠Ï†ú ÎÑ§Ìä∏ÏõåÌÅ¨ Ïò§Î•ò:', error);
      if (error.config) {
        console.log('üîç [TargetModelList] OnPrem ÏÇ≠Ï†ú ÏöîÏ≤≠ Ï†ïÎ≥¥:', {
          method: error.config.method?.toUpperCase(),
          url: error.config.url,
          baseURL: error.config.baseURL,
          fullURL: `${error.config.baseURL}${error.config.url}`,
          data: error.config.data
        });
      }
    });
    
    deletePromises.push(onPremDeletePromise);
  }

  console.log('‚è≥ [TargetModelList] ÏÇ≠Ï†ú API Promise Ïã§Ìñâ Ï§ë...');
  Promise.all(deletePromises)
    .then(res => {
      console.log('‚úÖ [TargetModelList] ÏÇ≠Ï†ú ÏÑ±Í≥µ:', res);
      handleRefreshTable();
      tableModel.tableState.selectIndex = [];
      showSuccessMessage('success', 'Delete Success');
    })
    .catch(err => {
      console.error('‚ùå [TargetModelList] ÏÇ≠Ï†ú Ïã§Ìå®:', err);
      showErrorMessage('Error', err);
    });
}

/**
 * Refresh table data
 * ÌÖåÏù¥Î∏î Îç∞Ïù¥ÌÑ∞Î•º ÏÉàÎ°úÍ≥†Ïπ®Ìï©ÎãàÎã§.
 */
function handleRefreshTable() {
  getTableList();
}

/**
 * Handle delete confirmation
 * ÏÇ≠Ï†ú ÌôïÏù∏ÏùÑ Ï≤òÎ¶¨Ìï©ÎãàÎã§.
 */
function handleDeleteConfirm() {
  console.log('‚úÖ [TargetModelList] ÏÇ≠Ï†ú ÌôïÏù∏ Î≤ÑÌäº ÌÅ¥Î¶≠ - ÏÇ≠Ï†ú Ïã§Ìñâ');
  multiDelete();
  modals.alertModalState.open = false;
  console.log('üîí [TargetModelList] ÏÇ≠Ï†ú ÌôïÏù∏ Î™®Îã¨ Îã´Í∏∞');
}
</script>

<template>
  <div>
    <p-horizontal-layout :height="400" :min-height="400" :max-height="1000">
      <template #container="{ height }">
        <p-toolbox-table
          ref="toolboxTable"
          :items="tableModel.tableState.displayItems"
          :fields="tableModel.tableState.fields"
          :total-count="tableModel.tableState.tableCount"
          :style="{ height: `${height}px` }"
          :sortable="tableModel.tableOptions.sortable"
          :sort-by="tableModel.tableOptions.sortBy"
          :selectable="tableModel.tableOptions.selectable"
          :multi-select="tableModel.tableOptions.multiSelect"
          :search-type="tableModel.tableOptions.searchType"
